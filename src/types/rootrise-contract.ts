  // Utility functions specific to RootRise
  export const getProjectStatus = (project: Project): ProjectStatus => {
    const now = BigInt(Math.floor(Date.now() / 1000));
    
    if (!project.isOpen) {
      return ProjectStatus.CANCELLED;
    }
    
    if (project.deadline < now) {
      if (project.amountRaised >= project.goal) {
        return ProjectStatus.FUNDED;
      } else {
        return ProjectStatus.EXPIRED;
      }
    }
    
    return ProjectStatus.ACTIVE;
  };
  
  export const calculateProgress = (project: Project): number => {
    if (project.goal === 0n) return 0;
    return Math.min(100, Number((project.amountRaised * 100n) / project.goal));
  };
  
  export const getDaysRemaining = (deadline: bigint): number => {
    const now = Math.floor(Date.now() / 1000);
    const deadlineSeconds = Number(deadline);
    const remainingSeconds = deadlineSeconds - now;
    return Math.max(0, Math.ceil(remainingSeconds / (24 * 60 * 60)));
  };
  
  export const isProjectFunded = (project: Project): boolean => {
    return project.amountRaised >= project.goal;
  };
  
  export const isProjectExpired = (project: Project): boolean => {
    const now = BigInt(Math.floor(Date.now() / 1000));
    return project.deadline < now;
  };
  
  export const canClaimRefund = (project: Project): boolean => {
    return isProjectExpired(project) && !isProjectFunded(project) && project.amountRaised > 0n;
  };
  
  export const canDisburseFunds = (project: Project): boolean => {
    return isProjectFunded(project) && !project.fundsDisbursed && project.isOpen;
  };
  
